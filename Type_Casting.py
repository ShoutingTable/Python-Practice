#Type Casting = Convert the data type of a value to another data type

x = 1 #int
y = 2.0 #float
z = "3" #str

x = float(x) #type cast x as a float
y = int(y) #type cast y as a integer
z = int(z) #type cast z as a integer

print(x)
print(y)
print(z)

print("X is " + str(x))
print("Y is " + str(y))
print("Z is " + str(z))


