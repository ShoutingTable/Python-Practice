#String Methods

name = "chrisss"

print(len(name)) #len is the length variable
print(name.find("i")) #name.find finds the index of the letter 
print(name.capitalize()) #this capitalizes your name
print(name.lower()) #lower cases the the first letter in the name
print(name.isdigit()) #returns true or false if string is a digit
print(name.isalpha()) #isalpha asks if the string has letters
print(name.count("s")) #counts the number of letters
print(name.replace("s"," ")) #replaces letter 
print(name*3) #prints the name 3 times