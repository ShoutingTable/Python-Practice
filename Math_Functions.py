#Math Functions

import math #math module

pi = 3.14
p1 = -3
x = 1
y = 2
z = 4

print(round(pi)) #rounds value
print(math.ceil(pi)) #rounds up
print(math.floor(pi)) #rounds down
print(abs(p1)) #absolute value of a number
print(pow(pi, 2)) #power function
print(math.sqrt(pi)) #square root
print(max(p1, pi, x, y, z)) #finds the max value 
print(min(pi, p1, x, y, z)) #finds the min value